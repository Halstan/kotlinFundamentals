package loops

fun main(){
    //THE RESERVED WORD 'step' IS EQUALS THAN i++
    //EXAMPLE: KOTLIN: 'step 2', JAVA: 'i + 2'

    for (i in 1..4) {  // equivalent of 1 <= i && i <= 4
        println(i)
    }

    println("-------------------------")

    //-----------------------------------------------------------------------------------

    for (i in 4 downTo 1) {
        println(i)
    }

    println("-------------------------")

    //-----------------------------------------------------------------------------------

    for (i in 1..8 step 2) {
        println(i)
    }
    println("-------------------------")
    for (i in 8 downTo 1 step 2) {
        println(i)
    }

    println("-------------------------")

    //-----------------------------------------------------------------------------------

    for (i in 1 until 10) {       // i in [1, 10), 10 is excluded
        println(i)
    }

    println("-------------------------")

    //-----------------------------------------------------------------------------------

    println((1..10).filter { it % 2 == 0 })
    println("-------------------------")

    
}